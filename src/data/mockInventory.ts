function line(
  id: string,
  sku: string,
  barcode: string,
  name: string,
  brandId: string,
  quantity: number,
  status: LineStatus = 'Available',
  batchNo: string | null = null
): InventoryLineItem {
  return { id, sku, barcode, name, batchNo, status, quantity, brandId }
}

function hierarchyForCode(code: string, aisleId: string, aisleLabel: string, bayId: string, bayLabel: string) {
  const parts = parseLocationCode(code)
  if (parts) {
    return {
      path: hierarchyPathForLocation(parts),
      labels: [
        'Warehouse',
        `${quadrantLabel(parts.quadrant)} (${parts.quadrant})`,
        `Aisle ${parts.aisle}`,
        `Rack ${parts.rack}`,
        `Bay ${parts.bay}`,
        `Shelf ${parts.shelf}`,
      ],
    }
  }
  if (code.startsWith('D-')) {
    return {
      path: ['wh-main', 'quad-w', 'aisle-dock', bayId],
      labels: ['Warehouse', 'West (W)', 'Dock staging', bayLabel],
    }
  }
  if (code.startsWith('INSP-')) {
    return {
      path: ['wh-main', 'quad-w', 'aisle-inspection', code.toLowerCase()],
      labels: ['Warehouse', 'West (W)', 'Inspection', code],
    }
  }
  return {
    path: ['wh-main', 'quad-w', aisleId, bayId],
    labels: ['Warehouse', 'West (W)', aisleLabel, bayLabel],
  }
}

function loc(opts: {
  id: string
  code: string
  zone: ZoneType
  kind: LocationKind
  aisleId: string
  aisleLabel: string
  bayId: string
  bayLabel: string
  brandId: string | null
  maxUnits: number
  filledUnits: number
  fillPercent: number
  items: InventoryLineItem[]
}): InventoryLocation {
  const hier = hierarchyForCode(opts.code, opts.aisleId, opts.aisleLabel, opts.bayId, opts.bayLabel)
  return {
    id: opts.id,
    locationCode: opts.code,
    zoneType: opts.zone,
    locationKind: opts.kind === 'product_shelf' && opts.zone === 'inspection' ? 'inspection_hold' : opts.kind,
    hierarchyPath: hier.path,
    hierarchyLabels: hier.labels,
    brandId: opts.brandId,
    maxUnits: opts.maxUnits,
    filledUnits: opts.filledUnits,
    fillPercent: opts.fillPercent,
    lineItems: opts.items,
    scanBarcode: opts.zone === 'pick' ? null : null,
  }
}

